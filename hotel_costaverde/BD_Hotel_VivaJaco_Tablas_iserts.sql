-- TABLAS

CREATE TABLE hotel (
    id_hotel NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(100),
    direccion VARCHAR2(200),
    ciudad VARCHAR2(100),
    pais VARCHAR2(100),
    telefono VARCHAR2(50),
    email VARCHAR2(100)
);

-- Tabla habitaciones
CREATE TABLE habitaciones (
    id_habitacion NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_hotel NUMBER,
    numero VARCHAR2(20),
    tipo VARCHAR2(50),
    precio_por_noche NUMBER(10, 2),
    capacidad NUMBER,
    estado VARCHAR2(50),
    FOREIGN KEY (id_hotel) REFERENCES hotel(id_hotel)
);

-- Tabla clientes
CREATE TABLE clientes (
    id_cliente NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(100),
    email VARCHAR2(100),
    telefono VARCHAR2(50),
    cedula VARCHAR2(20),
    fecha_registro DATE
);

-- Tabla reservas
CREATE TABLE reservas (
    id_reserva NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente NUMBER,
    id_habitacion NUMBER,
    fecha_entrada DATE,
    fecha_salida DATE,
    estado VARCHAR2(50),
    FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente),
    FOREIGN KEY (id_habitacion) REFERENCES habitaciones(id_habitacion)
);

-- Tabla servicios
CREATE TABLE servicios (
    id_servicio NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(100),
    precio NUMBER(10, 2)
);

-- Tabla reserva_servicio
CREATE TABLE reserva_servicio (
    id_reserva NUMBER,
    id_servicio NUMBER,
    cantidad NUMBER,
    PRIMARY KEY (id_reserva, id_servicio),
    FOREIGN KEY (id_reserva) REFERENCES reservas(id_reserva),
    FOREIGN KEY (id_servicio) REFERENCES servicios(id_servicio)
);

-- Tabla facturas
CREATE TABLE facturas (
    id_factura NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_reserva NUMBER,
    fecha_emision DATE,
    monto_total NUMBER(10, 2),
    metodo_pago VARCHAR2(50),
    estado VARCHAR2(50),
    FOREIGN KEY (id_reserva) REFERENCES reservas(id_reserva)
);

-- Tabla roles
CREATE TABLE roles (
    id_rol NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_rol VARCHAR2(50)
);

-- Tabla permisos
CREATE TABLE permisos (
    id_permiso NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_permiso VARCHAR2(100)
);

-- Tabla rol_permiso
CREATE TABLE rol_permiso (
    id_rol NUMBER,
    id_permiso NUMBER,
    PRIMARY KEY (id_rol, id_permiso),
    FOREIGN KEY (id_rol) REFERENCES roles(id_rol),
    FOREIGN KEY (id_permiso) REFERENCES permisos(id_permiso)
);

-- Tabla usuarios
CREATE TABLE usuarios (
    id_usuario NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(100),
    email VARCHAR2(100),
    password VARCHAR2(200),
    id_rol NUMBER,
    FOREIGN KEY (id_rol) REFERENCES roles(id_rol)
);
